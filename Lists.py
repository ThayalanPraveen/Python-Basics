# Lists can hold multiple data types in them

list1 = ['1', 2, 'cat', 12, 23]
print(list1)

# .append() will add a new element to the end of the list
list1.append('apple')
print(list1)

# list indexing
x = list1[3]  # This will take the item at index 3 from list and pass it to variable x
print(x)




